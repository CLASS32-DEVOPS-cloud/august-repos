The git process
mkdir to creat the directory
cd directory name to access it
You can either creat the files manually or you can use command prompt.
use git init to initialise versions control system whose aim is to allow collaboration, track history of changes made on a code.
git add . to add all the changes
once git init is done, note that the directory becomes a local repo.
git commit -m and summary caption of the modificaiton done.
anything committed will be seen by other members. what you don't want to move to the remote, or be seen by others, do not commit.
